#include "push_swap.h"

int	ft_isdigit(char c)
{
	return ('0' <= c && c <= '9');
}

int	ft_issign(char c)
{
	return (c == '-' || c == '+');
}

int	ft_isspace(char c)
{
	return (c == '\t' || c == '\n' || c == '\r' || \
			c == '\f' || c == '\v' || c == ' ');
}
